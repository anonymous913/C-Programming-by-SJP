// Assignment Operators

    #include <stdio.h>

    int main(){

        int a =5,c;

        c = a;   // c is 5
        printf("%d\n",c);
        c += a;
        printf("%d\n",c);
        c -= a;
        printf("%d\n",c);
        c *= a;
        printf("%d\n",c);
        c /= a;
        printf("%d\n",c);
        c %= a;
        printf("%d\n",c);
        
        return 0;
        

    }